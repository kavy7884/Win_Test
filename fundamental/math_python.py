# pow
x = 10
print(x ** 2)

# multi-assignment
x, y, z = 10, 20, 30
print(x, y, z)

# divmod
x = 9 // 5
y = 9 % 5
print(x, y)
z = divmod(9, 5)
x, y = z
print(z)
print(x, y)

# multi-line
x = 3 + \
    4 + \
    5 + \
    6
print(x)

x = ( 3 + 
      4 +  # Can add comment  
      5 + 
      6 )
print(x)

